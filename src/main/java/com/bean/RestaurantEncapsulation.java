package com.bean;

public class RestaurantEncapsulation {
	private String Name;
	private String Soups;
	private String Starters;
	private String MainCourse;
	private String Desserts;
	private String Mocktails;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getSoups() {
		return Soups;
	}
	public void setSoups(String soups) {
		Soups = soups;
	}
	public String getStarters() {
		return Starters;
	}
	public void setStarters(String starters) {
		Starters = starters;
	}
	public String getMainCourse() {
		return MainCourse;
	}
	public void setMainCourse(String mainCourse) {
		MainCourse = mainCourse;
	}
	public String getDesserts() {
		return Desserts;
	}
	public void setDesserts(String desserts) {
		Desserts = desserts;
	}
	public String getMocktails() {
		return Mocktails;
	}
	public void setMocktails(String mocktails) {
		Mocktails = mocktails;
	}
	
	
}
